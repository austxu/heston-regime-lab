def _bs_abs_delta(
    spot: float,
    strike: float,
    rate: float,
    div_yield: float,
    tau: float,
    sigma: float,
    option_type: str,
) -> float:
    """|Black-Scholes delta| from market IV, for the deep-OTM cut."""
    from scipy.stats import norm

    if not np.isfinite(sigma) or sigma <= 0 or tau <= 0:
        return 0.0
    d1 = (np.log(spot / strike) + (rate - div_yield + 0.5 * sigma**2) * tau) / (
        sigma * np.sqrt(tau)
    )
    call_delta = np.exp(-div_yield * tau) * norm.cdf(d1)
    delta = call_delta if option_type == "call" else call_delta - np.exp(-div_yield * tau)
    return float(abs(delta))


def filter_liquid_options(
    snapshot: ChainSnapshot, config: dict
) -> tuple[pd.DataFrame, LiquidityReport]:
    """Drop illiquid / unusable quotes and report what was removed and why.

    Filters (config ``data.liquidity``):
      * no recoverable market IV (price below intrinsic or non-invertible),
      * very short maturity (microstructure noise dominates),
      * zero open interest,
      * bid-ask spread wider than ``max_rel_spread`` of the mid,
      * deep OTM with ``|delta| < min_delta``.
    """
    liq = config["data"]["liquidity"]
    df = snapshot.chain.copy()
    n_input = len(df)
    rep = LiquidityReport(n_input=n_input, n_kept=0)

    # No usable implied vol.
    bad_iv = ~np.isfinite(df["market_iv"]) | (df["market_iv"] <= 0)
    rep.removed_no_iv = int(bad_iv.sum())
    df = df[~bad_iv]

    # Short maturity.
    min_tau = float(liq["min_maturity_days"]) / 365.0
    short = df["maturity"] < min_tau
    rep.removed_short_maturity = int(short.sum())
    df = df[~short]

    # Zero open interest.
    zero_oi = df["open_interest"] < float(liq["min_open_interest"])
    rep.removed_zero_oi = int(zero_oi.sum())
    df = df[~zero_oi]

    # Wide relative spread (only where both bid/ask are present).
    rel_spread = (df["ask"] - df["bid"]) / df["mid"].replace(0, np.nan)
    wide = rel_spread.gt(float(liq["max_rel_spread"])).fillna(False)
    rep.removed_wide_spread = int(wide.sum())
    df = df[~wide]

    # Deep OTM by delta.
    deltas = (
        np.array(
            [
                _bs_abs_delta(snapshot.spot, k, snapshot.rate, snapshot.div_yield, t, iv, ot)
                for k, t, iv, ot in zip(
                    df["strike"], df["maturity"], df["market_iv"], df["option_type"]
                )
            ]
        )
        if len(df)
        else np.array([])
    )
    deep = deltas < float(liq["min_delta"]) if len(df) else np.array([], dtype=bool)
    rep.removed_deep_otm = int(deep.sum())
    df = df[~deep] if len(df) else df

    df = df.reset_index(drop=True)
    rep.n_kept = len(df)
    return df, rep

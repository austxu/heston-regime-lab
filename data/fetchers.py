def get_price_history(config: dict, prefer_live: bool = True) -> FetchResult:
    """SPX daily history, live if reachable else synthetic (with provenance)."""
    ticker = config["data"]["spx_ticker"]
    years = int(config["data"]["history_years"])
    if prefer_live:
        try:
            df = _call_with_timeout(
                _request_timeout(config), fetch_price_history_live, ticker, years
            )
            return FetchResult(df, "live", _now(), ticker)
        except DataUnavailable:
            pass
    return FetchResult(synthetic_price_history(config), "synthetic", _now(), ticker)


def get_vix_term_structure(
    config: dict, price_history: pd.DataFrame | None = None, prefer_live: bool = True
) -> FetchResult:
    """VIX term structure, live if reachable else synthetic.

    The synthetic path needs the synthetic price history (to share its regimes), so
    pass ``price_history`` when you already have a synthetic one.
    """
    tickers = config["data"]["vix_tickers"]
    years = int(config["data"]["history_years"])
    if prefer_live:
        try:
            df = _call_with_timeout(
                _request_timeout(config), fetch_vix_term_structure_live, tickers, years
            )
            return FetchResult(df, "live", _now(), "VIX")
        except DataUnavailable:
            pass
    if price_history is None or "_inst_vol" not in price_history.columns:
        price_history = synthetic_price_history(config)
    return FetchResult(synthetic_vix_term_structure(price_history), "synthetic", _now(), "VIX")


def get_risk_free_rate(config: dict, prefer_live: bool = True) -> float:
    """Risk-free proxy: FRED if a key is configured, else the config constant."""
    if prefer_live:
        key = os.environ.get(config["data"].get("fred_api_key_env", "FRED_API_KEY"))
        try:
            return _call_with_timeout(
                _request_timeout(config),
                fetch_fred_rate_live,
                config["data"]["fred_rate_series"],
                key,
            )
        except DataUnavailable:
            pass
    return float(config["market"]["risk_free_rate"])


def get_market_snapshot(
    config: dict, prefer_live: bool = True, allow_synthetic: bool = True
) -> ChainSnapshot:
    """A full options snapshot (spot + rate + chain), live-first with fallback.

    Spot is the latest close from the price history; the rate is the FRED/config
    risk-free proxy; the options chain is the live yfinance chain or the synthetic
    Heston surface.  This is exactly what the calibration service needs.

    With ``allow_synthetic=False`` the chain *must* come from a live pull — any failure
    raises :class:`DataUnavailable` instead of synthesising.  The cache uses this to
    distinguish "live failed, serve stale/synthetic" from a genuine live result.
    """
    div_yield = float(config["market"]["dividend_yield"])
    rate = get_risk_free_rate(config, prefer_live=prefer_live)
    today = _now()

    if prefer_live:
        timeout = _request_timeout(config)
        try:
            spot = float(_call_with_timeout(timeout, fetch_spot_live, config["data"]["spx_ticker"]))
            max_expiries = int(config["data"].get("max_option_expiries", 4))
            target_days = tuple(
                int(day)
                for day in config["data"].get("option_expiry_target_days", [30, 90, 180, 365])
            )
            chain = _call_with_timeout(
                timeout,
                fetch_options_chain_live,
                config["data"]["options_underlying"],
                spot,
                rate,
                div_yield,
                today,
                max_expiries,
                target_days,
            )
            if chain.empty:
                raise DataUnavailable("live chain empty")
            return ChainSnapshot(spot, rate, div_yield, chain, "live", today)
        except DataUnavailable:
            if not allow_synthetic:
                raise
    elif not allow_synthetic:
        raise DataUnavailable("live data disabled but synthetic not allowed")

    # Synthetic fallback uses a clean SPX-like spot so the surface is well-scaled.
    syn_spot = float(config["data"]["synthetic_fallback"]["spot"])
    chain = synthetic_options_chain(config, syn_spot, rate, div_yield, today)
    return ChainSnapshot(syn_spot, rate, div_yield, chain, "synthetic", today)



"""Market-data fetchers: SPX options chain, daily SPX/VIX history, FRED rates.

Design philosophy — **live with a deterministic offline fallback.**  Every public
``get_*`` helper first attempts a live pull (yfinance / FRED).  If the network, the
dependency, or the returned payload is unavailable or malformed, it falls back to a
*deterministic, seeded synthetic* generator so the entire research stack — calibration,
regime detection, the API — runs end-to-end with no network access.  Each result carries
a :class:`FetchResult.source` flag (``"live"`` / ``"synthetic"``) and an ``as_of``
timestamp so callers (and the API's staleness logic) always know what they are holding.

Public surface
--------------
    get_market_snapshot(config)      -> ChainSnapshot   # spot + rate + options chain
    get_price_history(config)        -> FetchResult[pd.DataFrame]
    get_vix_term_structure(config)   -> FetchResult[pd.DataFrame]
    get_risk_free_rate(config)       -> float
    filter_liquid_options(chain, cfg)-> (pd.DataFrame, LiquidityReport)

The synthetic price history is generated by a 3-regime volatility Markov chain, which is
what makes the downstream HMM recover *economically meaningful* regimes (calm / elevated
/ crisis) even offline.
"""

from __future__ import annotations

import os
from concurrent.futures import ThreadPoolExecutor
from concurrent.futures import TimeoutError as FuturesTimeout
from dataclasses import dataclass
from datetime import datetime, timezone

import numpy as np
import pandas as pd

from models.black_scholes import bs_price, implied_vol
from models.heston import HestonParams, heston_price


class DataUnavailable(RuntimeError):
    """Raised by the live-only fetchers when a live pull cannot be completed."""


@dataclass
class FetchResult:
    """A fetched dataset plus provenance.

    Attributes
    ----------
    data : Any
        The payload (a ``DataFrame``, ``Series`` or scalar).
    source : {"live", "synthetic"}
        Whether the data came from the network or the deterministic fallback.
    as_of : datetime
        UTC timestamp the data was produced/observed.
    symbol : str
        The instrument identifier, for diagnostics.
    """

    data: object
    source: str
    as_of: datetime
    symbol: str = ""


@dataclass
class ChainSnapshot:
    """A single-instant options snapshot, the unit calibration consumes.

    ``chain`` is a tidy DataFrame with one row per quoted option and the canonical
    columns produced by both the live and synthetic paths:
    ``expiry, maturity, strike, option_type, bid, ask, mid, last, volume,
    open_interest, market_iv``.
    """

    spot: float
    rate: float
    div_yield: float
    chain: pd.DataFrame
    source: str
    as_of: datetime


@dataclass
class LiquidityReport:
    """Counts of options removed by each liquidity filter, for diagnostics."""

    n_input: int
    n_kept: int
    removed_deep_otm: int = 0
    removed_zero_oi: int = 0
    removed_wide_spread: int = 0
    removed_short_maturity: int = 0
    removed_no_iv: int = 0

    def as_dict(self) -> dict:
        return {
            "n_input": self.n_input,
            "n_kept": self.n_kept,
            "removed_deep_otm": self.removed_deep_otm,
            "removed_zero_oi": self.removed_zero_oi,
            "removed_wide_spread": self.removed_wide_spread,
            "removed_short_maturity": self.removed_short_maturity,
            "removed_no_iv": self.removed_no_iv,
        }


CHAIN_COLUMNS = [
    "expiry",
    "maturity",
    "strike",
    "option_type",
    "bid",
    "ask",
    "mid",
    "last",
    "volume",
    "open_interest",
    "market_iv",
]


def _now() -> datetime:
    return datetime.now(timezone.utc)


# Bounded pool so a slow yfinance/FRED call can be abandoned via a timeout instead of
# hanging a request. The orphaned thread finishes in the background; we just stop waiting.
_EXECUTOR = ThreadPoolExecutor(max_workers=4, thread_name_prefix="hrl-fetch")


def _request_timeout(config: dict) -> float:
    timeout = float(config.get("data", {}).get("request_timeout", 8.0))
    if not np.isfinite(timeout) or timeout <= 0.0:
        raise ValueError(f"data.request_timeout must be finite and > 0, got {timeout!r}")
    return timeout


def _call_with_timeout(timeout: float, fn, *args, **kwargs):
    """Run a blocking live fetch with a hard timeout; raise DataUnavailable if it elapses."""
    future = _EXECUTOR.submit(fn, *args, **kwargs)
    try:
        return future.result(timeout=timeout)
    except FuturesTimeout as exc:
        future.cancel()
        raise DataUnavailable(f"live fetch timed out after {timeout:.0f}s") from exc


